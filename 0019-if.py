#  If
a = 10
b = 7

if a > b:
    print("a is greater than b")

print("----------")


# Elif
a = 10
b = 7

if a == b:
    print("a is equal b")
elif a > b:
    print("a is greater than b")


print("----------")


# Else
a = 10
b = 7

if a < b:
    print("a is lesser than b")
elif a == b:
    print("a is equal b")
else:
    print("a is greater than b")

print("----------")
