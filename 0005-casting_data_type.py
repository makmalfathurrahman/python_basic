# Casting integer to string
num_int = 123
num_str = str(num_int)
print("Integer to String:", num_str)
print("Type of num_str:", type(num_str))


# Casting string to integer
num_str = "456"
num_int = int(num_str)
print("String to Integer:", num_int)
print("Type of num_int:", type(num_int))

print("----------")


# Casting float to string
num_float = 2.5
